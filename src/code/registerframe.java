    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:

        String nombre=name.getText();
        String nombreusuario=username.getText();
        String contra=pass.getText();
        String tempgender =(String)gender.getSelectedItem();
        char genero=tempgender.charAt(0);
        if(name.getText().isBlank() || username.getText().isBlank() || pass.getText().isBlank() || age.getText().isBlank()){
            JOptionPane.showMessageDialog(null, "Porfavor, rellene todos los campos");
        } else {
            try{
                int edad=Integer.parseInt(age.getText());
                user reg=new user();
                if(reg.adduser(nombre, nombreusuario, contra, genero, edad)==true){
                    JOptionPane.showMessageDialog(null, "Cuenta creada con exito");
                    System.out.println("usuario logueado:"+reg.getLoggedinuser());
                    feedframe frame =new feedframe();
                    frame.setVisible(true);
                    this.dispose();
                } else {
                    JOptionPane.showMessageDialog(null, "Lo sentimos, ya existe un usuario con ese nombre");
                }
            }catch(Exception e){
                JOptionPane.showMessageDialog(null, "Porfavor, ingrese una edad valida");
            }
        }
        
        
        
        
    }//GEN-LAST:event_jButton1ActionPerformed

